public class java_exercise2 {
    public static void main(String[] args) {
        // encrypt the grade by 8
        char grade='B';
        grade=(char)(grade+8);
        System.out.println(grade);
        //decrypt the grade by 8
        grade =(char)(grade-8);
        System.out.println(grade);

    }
    
}
